							<h3>1. Was ist Open Parliament TV?</h3>
							<p>Open Parliament TV ist zunächst einmal eine <b>Suchmaschine und interaktive Videoplattform für Parlamentsdebatten</b>. Der Kern unserer Plattform ist eine technische Architektur, welche die <b>Videoaufzeichnungen</b> der Redebeiträge automatisiert <b>mit den Plenarprotokollen synchronisiert</b>.</p>
							<p>Hierüber können wir die Videos in einer <b>Volltextsuche</b> Wort für Wort durchsuchbar machen. Durch die Verknüpfung von Videoaufzeichnung und Protokolltext können wir die Reden zusätzlich</p>
							<ul>
								<li>um <b>interaktive Transkripte</b> erweitern (Klick auf einen Satz &gt; Sprung zu Zeitpunkt im Video)</li>
								<li>mit <b>Zusatzinformationen</b> verknüpfen (mehr Kontext zu erwähnten Personen, Themen, Gesetzen, Drucksachen und Fachbegriffen direkt im Videoplayer)</li>
								<li>mit <b>neuen Beteiligungsmöglichkeiten</b> besser zugänglich machen (zitieren, einbinden und teilen ausgewählter Videosegmente im Redekontext        </li>
							</ul>
							<p>Open Parliament TV ist ein Werkzeug, welches das <b>Auffinden</b>, <b>Teilen</b> und <b>Zitieren</b> von Videoausschnitten aus Parlamentsreden enorm erleichtert. So lassen sich basierend auf einzelnen Schlüsselwörtern oder Satzbausteinen in Sekundenbruchteilen die entsprechenden Ausschnitte finden, abspielen und dann als Zitat in andere Plattformen einbinden.</p>
							<p>Neben der Volltextsuche ermöglicht Open Parliament TV noch viele <b>weitere Einstiege in die Debatten</b>. So ist es beispielsweise möglich, alle Reden anzusehen, in denen eine bestimmte Drucksache oder ein bestimmtes Gesetz erwähnt wird.</p>
							<p>Open Parliament TV ist <b><a href="https://github.com/OpenParliamentTV" target="_blank">open source</a></b> und <b>parlamentsübergreifend</b>. Die gesamte Architektur wurde von Anfang an darauf ausgelegt, auf alle Parlamente übertragbar zu sein, welche Videoaufzeichnungen und Textprotokolle der Sitzungen veröffentlichen.</p>
							<p>Seit Oktober 2021 ist mit dem Deutschen Bundestag das erste Parlament auf Open Parliament TV zugänglich. Unter <a href="https://de.openparliament.tv" target="_blank" >https://de.openparliament.tv</a> sind mittlerweile über 60.000 Redebeiträge aus 10 Jahren Bundestagsgeschichte zugänglich, und es werden täglich mehr.</p>
