							<h3>4. Zielgruppen und Stakeholder</h3>
							<p>Open Parliament TV ist primär ein Werkzeug für <b>Journalist:innen</b> und <b>Aktivist:innen</b>. Indirekt möchten wir über diese zwei Zielgruppen <b>politisch interessierte Bürger:innen</b> erreichen (siehe auch <a href="#impact-logic">3. Wirkungslogik</a>)</p>
							<p><b>Journalist:innen</b> und <b>Aktivist:innen</b> (konkret: Watchdog Organisationen) ordnen Abläufe in den Parlamenten ein, beteiligen sich an kritischen Analysen und Diskussionen politischer Standpunkte und setzen die Aussagen von Abgeordneten in neue Kontexte.</p>
							<p>Die hierbei veröffentlichten Videoinhalte basieren jedoch meist auf kurzen, prägnanten Momenten ohne einen Verweis auf die gesamte Debatte, relevante Dokumente oder Zusatzmaterialien. Sobald Redeinhalte für kritische Analysen, Hintergrundartikel, Faktenchecks oder ausführliche Reportagen verwendet werden, ist die Basis der Text der Plenarprotokolle. Denn die Videos sind weder nach Schlagworten durchsuchbar, noch lassen sich einzelne Aussagen daraus zitieren oder verlinken.  </p>
							<p>Wir machen Videos von Parlamentsdebatten besser journalistisch nutzbar, indem wir das <b>Auffinden</b>, <b>Teilen</b>, <b>Zitieren</b> und <b>Einbetten</b> von Videoausschnitten aus Parlamentsreden erleichtern, die Einbettung <b>aktueller Berichterstattung</b> in Redebeiträge ermöglichen sowie Tools zur statistischen <b>Auswertung</b> und <b>Analyse</b> von Parlamentsdebatten anbieten.</p>
							<p>In Zukunft sollen politische Diskurse auf unserer Suchmaschine und interaktiven Videoplattform in Zusammenarbeit mit <b>Parlamentsverwaltungen</b> über Parlaments-, Landes- und Sprachgrenzen hinweg verknüpft und digital zugänglich werden.</p>
							<p>Neben den primären Zielgruppen und Stakeholdern sind politische <b>Fraktionen</b>, <b>Forschungsgruppen</b> und <b>Bildungseinrichtungen</b> durch ihre kontinuierliche Arbeit mit Parlamentsinhalten wichtige Stakeholder.</p>
							<p>So beschäftigen sich bspw. <b>Forschungsgruppen</b> bereits seit vielen Jahren in internationalen Konsortien mit der Frage der Interoperabilität und Standardisierung von Parlamentsinhalten und Abläufen (Stichwort maschinenlesbare Plenarprotokolle und Gesetzgebungsverfahren).</p>
							<p>Über Modellprojekte und Kooperationen mit <b>Bildungseinrichtungen</b> werden wir Tools und didaktische Konzepte zur manuellen Annotation / Verknüpfung der Redebeiträge mit Lehr- / Lernmaterialien entwickeln. Hierdurch verbessern wir die Nutzbarkeit von Parlamentsinhalten im Bereich der politischen Bildung.</p>
