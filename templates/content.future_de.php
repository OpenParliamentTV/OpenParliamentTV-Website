							<h3>5. Gemeinnütziges Geschäftsmodell</h3>
							<p>Die Entwicklung und Implementierung eines nachhaltigen <b>Geschäftsmodells</b> ist ein zentraler Bestandteil der 4-jährigen Skalierungsphase und wird in enger Zusammenarbeit zwischen der Projektleitung und der neu zu besetzenden Rolle im Bereich Geschäftsentwicklung erarbeitet werden. Die im Folgenden beschriebenen Ideen sind daher ebenso wie unser <b>Business Model Canvas</b> ein vorläufiger Entwurf.</p>
							<hr>
							<p><b>Grundsätze</b></p>
							<p>Im Sinne unserer Zielsetzung, Parlamentsdebatten transparenter und besser zugänglich zu machen, werden alle Bausteine der Open Parliament TV Plattform dauerhaft für alle Menschen unter <b>freien Lizenzen</b> veröffentlicht und sind dezentral implementier- und frei weiterentwickelbar. Unser Geschäftsmodell folgt dem Grundsatz der <b>politischen Unabhängigkeit</b>, ist <b>nicht-kommerziell</b> und unterliegt unserem <b><a href="https://openparliament.tv/code-of-conduct" target="_blank">Code of Conduct</a></b>.</p>
							<hr>
							<p><b>Finanzierung</b></p>
							<p>Die Open Parliament TV Idee lässt sich prinzipiell weltweit auf <b>alle nationalen und regionalen Parlamente</b> anwenden, die Videoaufzeichnungen und Protokolle der Sitzungen veröffentlichen (unsere technische Architektur wurde von Anfang an darauf ausgelegt). Durch jedes Parlament vergrößern sich auch die potentiellen Zielgruppen und Stakeholder. Darüber hinaus sind die Themen <b>Verwaltungsdigitalisierung</b>, <b>Transparenz</b>, <b>offene Daten</b>, <b>Stärkung der Demokratie</b> und <b>Bekämpfung von Desinformation</b> sehr aktuell.</p>
							<p>Wir sehen hierin ein großes <b>Skalierungspotential</b> sowohl für die Erweiterung unserer Suchmaschine und interaktiven Videoplattform als auch für die zukünftige Finanzierung unserer Arbeit an Open Parliament TV.</p>
							<p>In den letzten 5 Jahren wurden wir durch Stiftungen, Demokratie-Förderprogramme, Innovationsförderung und einzelne Großspenden unterstützt. Zukünftig planen wir die <b>Weiterentwicklung</b> über Modellprojekte, Forschungsvorhaben und öffentliche Förderung zu finanzieren. Der <b>laufende Betrieb</b> soll ab 2027 in einem gemeinnützigen Geschäftsmodell von institutionellen Nutzer:innen & Spender:innen und bis dahin durch Stiftungen finanziert werden.</p>
							<p>In diesem Rahmen werden wir für bestimmte Zielgruppen spezielle <b>Produkte</b> und <b>Dienstleistungen</b> entwickeln, welche den klassischen Modellen der Monetarisierung von Open Source Projekten folgen und uns langfristig finanziell unabhängig von Fördermitteln machen sollen.  </p>
							<p><b>Produkte</b></p>
							<ul>
								<li>Plattform-Services</li>
								<ul>
									<li>Erweiterte Exportformate (bspw. Schnittstelle zu Videoproduktions-Tools)</li>
									<li>Video Annotations-Tool</li>
									<li>Hochladen eigener Zusatzmaterialien in geschützten Bereichen und privaten Gruppen (bspw. für Schulklassen und Politik-Seminare)</li>
								</ul>
							</ul>
							<ul>
								<li>White-Label Lösungen</li>
								<ul>
									<li>Parlaments-Mediathek as a Service</li>
									<li>Videoplayer und Social Media Snippets mit eigenem Branding</li>
								</ul>
							</ul>
							<ul>
								<li>Politik Monitoring-Lösungen</li>
								<ul>
									<li>Automatische Zusammenfassungen von Debatten</li>
									<li>Benachrichtigungen bei Keywords in neuen Debatten</li>
									<li>Benachrichtigungen bei Häufung von Begriffen</li>
									<li>Individuelle Statistiken und Auswertungen</li>
								</ul>
							</ul>
							<p>Angedacht ist zudem eine zweite, von der Suchmaschine und interaktiven Videoplattform für Parlamentsdebatten getrennte Plattform, welche insbesondere die Annotations-Funktionalitäten (Verknüpfen der Videos mit Zusatzinformationen zu bestimmten Zeitpunkten) sowie die Synchronisation von Videos und Transkripten für andere Domänen zugänglich macht (bspw. Lehr- / Lernvideos, Erklärvideos, Vorlesungsaufzeichnungen, Nachrichtensendungen und Politikmagazine).  </p>
							<p><b>Dienstleistungen</b></p>
							<ul>
								<li>Maßgeschneiderte Entwicklungen</li>
								<li>spezielle Schnittstellen / Integrationen (bspw. in Redaktionssysteme)</li>
								<li>Plug-Ins für bestehende Tools / Software</li>
								<li>Beratung & Consulting für Parlamentsverwaltungen (Open Data Standards, Interoperabilität, Schnittstellen, Dokumentenformate)</li>
							</ul>
							<p>Darüber hinaus planen wir in Zusammenarbeit mit <b>Medienpartnern</b> gemeinsame Recherchen und inhaltliche Auswertungen, Faktenchecks zu Parlamentsdebatten, experimentelle Storytelling Formate und Datenvisualisierungen sowie die Entwicklung von Kampagnen-Tools und Widgets vor anstehenden Wahlen und bei wichtigen Debatten.</p>
