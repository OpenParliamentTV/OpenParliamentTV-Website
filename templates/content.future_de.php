							<h3>3. Wirkungslogik</h3>
							<p>Um demokratische Prozesse und Mitbestimmungsmöglichkeiten besser in unseren digitalisierten Alltag integrieren zu können, müssen sie für alle Menschen leicht, zeitnah und direkt zugänglich sein.</p>
							<p>Videos von Parlamentsdebatten dienen uns in diesem Kontext als greifbare, zeitgemäße und tagesaktuelle Schnittstelle zur Arbeit der Parlamente. Die Aufzeichnungen und Live-Übertragungen sind nicht nur eine Materialsammlung für Journalist:innen oder Mitarbeitende des Parlamentsbetriebs, sondern eine unmittelbare Umsetzung des Credos „Das Parlament verhandelt öffentlich“.</p>
							<p>Wir stärken mit Open Parliament TV das <b>Vertrauen in die parlamentarische Demokratie</b>, indem wir</p>
							<ul>
								<li>Parlamentsdebatten <b>leichter zugänglich</b> und <b>besser nutzbar</b> machen</li>
								<li>dem Parlamentsgeschehen insgesamt <b>mehr Sichtbarkeit</b> verschaffen</li>
							</ul>
							<p>Hierbei geht es uns am Ende darum, die Zielgruppe der politisch interessierten Bürger:innen zu erreichen. Da wir jedoch selbst keine inhaltlichen Einordnungen vornehmen und sich die Kontextualisierung mit Zusatzinhalten auf automatisierte Verfahren beschränkt, fokussieren wir uns in unserer Wirkungslogik auf die zwei Zielgruppen Journalist:innen und Aktivist:innen (siehe auch <a href="#target-groups">4. Zielgruppen und Stakeholder</a>). Diese betten die Debatten durch Analysen, Recherchen, Hintergrundberichte, Fact Checking Formate und Kampagnen in einen inhaltlichen Kontext ein, welchen wir als Open Parliament TV nicht bieten können und wollen.</p>
							<p>Wir sehen unsere Stärke in der Entwicklung und Bereitstellung technologischer Lösungen als “Werkzeug” zur Arbeit mit Parlamentsinhalten. Unsere Suchmaschine und interaktive Videoplattform für Parlamentsdebatten ist ein Baustein einer neu zu schaffenden <b>“Basisarchitektur” der digitalen Demokratie</b>, welche einen unabhängigen und parlamentsübergreifenden Zugriff auf Inhalte bietet.</p>
							<p><b>Wirken “über Bande”</b></p>
							<p>Wir wirken mit unserer Arbeit indirekt. Indem Journalist:innen und Aktivist:innen unsere Werkzeuge nutzen, bekommt das Parlamentsgeschehen insgesamt mehr Sichtbarkeit. Wir sind davon überzeugt, dass wir auf diesem Weg am Ende deutlich mehr Menschen erreichen und im Sinne unserer <b>Wirkungslogik</b> deutlich breiter und nachhaltiger wirken können. Ein Nebeneffekt der Wirkung “über Bande” ist zudem, dass es uns dies einfacher macht, eindeutige und messbare (SMART) Ziele zu setzen.</p>
