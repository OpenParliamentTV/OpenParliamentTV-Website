		<section id="strategy">
			<div class="container">
				<div class="row">
				  <div class="col-lg-9 mx-auto card" style="border-color: #76b88a">
					<div class="card-body">
						<h3 style="color: #76b88a;">Strategie</h3>
						<p>Um unsere Vision einer vertrauens- und glaubwürdigen parlamentarischen Demokratie durch mehr Transparenz, Bildung und Beteiligung umzusetzen, fokussieren wir uns auf drei miteinander eng verbundene strategische Bereiche: <strong>Technologie</strong>, <strong>Policy & Advocacy</strong>, <strong>Netzwerk</strong>.</p>
						<p><strong><u>Technologie:</u></strong></p><p>Fast jedes Parlament veröffentlicht Videoaufzeichnungen und Protokolle der Sitzungen. Zusammen mit anderen Dokumenten wie Anträgen, Gesetzesentwürfen oder Anfragen erlauben sie einen direkten Einblick in die Entscheidungsprozesse der parlamentarischen Demokratie. </p>
						<p>Doch trotz vergleichbarer Strukturen und ähnlicher Arbeitsabläufe werden die Protokolle in verschiedenen, nicht kompatiblen Formaten veröffentlicht und die Inhalte des Parlamentsfernsehens sind nur über in sich geschlossene Plattformen zugänglich. </p>
						<p>In Videoaufzeichnungen von Parlamentsdebatten steckt ein großes ungenutztes Potential für die Digitale Demokratie. Sie sind greifbarer als Textdokumente oder Abstimmungslisten und ein leicht zugängliches „Fenster“ zu den dahinter liegenden Diskussionen, Verhandlungen und Entscheidungen. </p>
						<p>Wir wollen dieses Potential nutzen, indem wir </p>
						<ul>
							<li>eine gemeinsame technologische Basis für Parlamente schaffen (interoperable Standards, Schnittstellen und Dokumentenformate) sowie </li>
							<li>eine Suchmaschine und interaktive Videoplattform entwickeln, auf der die Redebeiträge über eine Synchronisation von Videos und Protokollen parlamentsübergreifend durchsuch-, verknüpf-, verlink-, zitier- und teilbar sind.</li>
						</ul>
						<p>Um mehr Beteiligung und Dialog zu ermöglichen, schaffen wir technische Schnittstellen zu bestehenden zivilgesellschaftlichen Demokratieförderprojekten und ermöglichen Journalist:innen Parlamentsdebatten mit Hintergrundinformationen, Originalquellen oder Faktenchecks zu verknüpfen. </p>
						<p>Alle Bausteine werden unter freien Lizenzen veröffentlicht und sind dezentral implementier- und frei weiterentwickelbar. Neue Parlamente können entweder in bestehende Open Parliament TV Plattformen integriert oder gänzlich unabhängig aufgebaut werden. Über offene Schnittstellen sind alle Inhalte trotzdem parlamentsübergreifend zugänglich, durchsuchbar und verknüpfbar. </p>
						<p><strong><u>Policy & Advocacy:</u></strong></p>
						<p>Die Digitalisierung der Verwaltungsarbeit wird in vielen Parlamenten von mangelnden Ressourcen, fehlenden rechtlichen Rahmenbedingungen, ausgelagertem Fachwissen sowie veralteten IT- und Beratungsstrukturen verlangsamt. Zudem fehlt sowohl auf der politischen als auch auf der Verwaltungsebene oft das Verständnis für die Notwendigkeit und die Strukturen zur Vernetzung, Abstimmung und konkreten Zusammenarbeit mit anderen Parlamenten. </p>
						<p>Die Entscheidungen über rechtliche Rahmenbedingungen, die Anwendung technologischer Standards oder den offenen Zugang zu Daten werden aber nicht auf Verwaltungs- sondern auf politischer Ebene getroffen. </p>
						<p>Wir engagieren uns daher auf allen politischen Ebenen für </p>
						<ul>
							<li>einen Rechtsanspruch auf offene Daten (Linked Open Data),</li>
							<li>die freie Lizenzierung von Inhalten und Daten aus den Parlamenten, </li>
							<li>den Zugang zu Quellcode und die freie Lizenzierung aller öffentlich finanzierten Softwarekomponenten (Free and Open Source), </li>
							<li>die Einhaltung und Weiterentwicklung bestehender Standards für Schnittstellen, Videos, Dokumente und deren Metadaten, </li>
							<li>den Aufbau interner Kompetenzen zur konsequenten Umsetzung von Linked Open Data, </li>
							<li>die Veröffentlichung und Langzeitarchivierung der Videoaufzeichnungen von Parlamentsdebatten (wo das bisher nicht der Fall ist), Anhörungen und Ausschusssitzungen sowie </li>
							<li>die dauerhafte öffentliche Finanzierung der hierfür notwendigen Infrastruktur und Prozesse. </li>
						</ul>
						<p>Um diese Ziele langfristig auf der politischen Agenda zu verankern, leisten wir Aufklärungs- und Überzeugungsarbeit bei Entscheidungsträger:innen in Fraktionen und Parlamentspräsidien. </p>
						<p>Wir beteiligen uns aktiv an Open Government Arbeit und bringen uns in bestehende Bündnisse, Verbände, Netzwerke und Communities ein. Wir treten in Arbeitsgruppen, Gremien und auf Veranstaltungen für unsere Vision und Mission ein und stärken das Bewusstsein für die direkte Verbindung von transparenten politischen Prozessen und einer vertrauens- und glaubwürdigen parlamentarischen Demokratie. </p>
						<p><strong><u>Netzwerk:</u></strong></p>
						<p>Um unser Ziel einer parlamentsübergreifenden Verknüpfung politischer Debatten effektiv umsetzen zu können, bauen wir ein eigenes Netzwerk auf, in dem wir uns spezifisch auf die Nutzung von Parlamentsdaten und Videoaufzeichnungen von Parlamentsdebatten fokussieren. </p>
						<p>Hierzu schaffen wir Kommunikationswege und regelmäßige Austauschformate zwischen </p>
						<ul>
							<li>Sprach-, Sozial-, Informations- und Politikwissenschaften, </li><li>Parlamentsverwaltungen, </li>
							<li>Journalist:innen und News Labs, </li>
							<li>Parliamentary Monitoring Organisations (PMOs) und </li>
							<li>Open Data Aktivist:innen, </li>
						</ul>
						<p>welche in ihren Organisationen und Communities bereits seit vielen Jahren an Dokumentenstandards und standardisierten Datenschnittstellen für Parlamente arbeiten. </p>
						<p >Gemeinsam mit unserem Netzwerk setzen wir uns dafür ein, dass Strukturen zur besseren Zusammenarbeit der Parlamente geschaffen werden und die verschiedenen Rechtsordnungen, Sprachen, parlamentarischen Systeme und technologischen Gegebenheiten bei der Entwicklung internationaler Standards berücksichtigt werden.</p>
						<p>Wir fördern aktiv den grenz- und disziplinübergreifenden Wissensaustausch, schaffen eine gemeinsame Wissensbasis und bauen hierfür eigene Ressourcen auf. </p>
