								<p>Videos of parliamentary debates serve as a tangible and contemporary interface to the daily work of parliaments. The recordings and livestreams are not just video collections for journalists or members of parliament but a direct application of the guiding principle “the parliament negotiates in public“. </p>
								<p>With Open Parliament TV, we want to strengthen Digital Democracy by </p>
								<ul>
