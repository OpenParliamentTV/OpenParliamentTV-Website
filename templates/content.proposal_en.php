								<p class="mt-3">Parliamentary debates are a regular topic in news, on social media or in talk shows. In their function as “fourth estate“, journalists interpret developments in parliaments, critically analyse and discuss political standpoints and contextualise statements of politicians. </p>
								<p>But the way videos of speeches in parliament are currently published is largely based upon sharing short key moments, with a clear lack of contextual information like a link to the full debate, relevant original documents or additional materials. This means video clips are used to share short moments, but as soon as the speeches are used for in-depth analyses, background articles, fact checks or longform reporting, the basis are the textual proceedings. Because the videos are neither searchable by keywords nor can specific statements be cited or directly interlinked. </p>
