						<p><strong><u>Transparency:</u></strong> We make political work visible. Political discourse, debates, decision processes and the formation process of laws have to be transparent and comprehensible on regional, national and international levels. We thus advocate for open interfaces to allow citizens unrestricted access to parliamentary data. </p>
						<p><strong><u>Education:</u></strong> We support political education by conveying parliamentary debates, offer background information, interconnect political discourse and debates and uncover misinformation. We help political institutions to build up the knowledge necessary for digitising existing structures.</p>
						<p><strong><u>Participation</u></strong><u>:</u> We create means of participation for civil society. By breaking open existing structures, facilitating new networks and making data usable, we help citizens to engage, discuss and take part in politics. We pay close attention to accessible design and implementation of all means of participation. </p>
