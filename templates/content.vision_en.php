		<section id="strategy">
			<div class="container">
				<div class="row">
				  <div class="col-lg-9 mx-auto card" style="border-color: #76b88a">
					<div class="card-body">
						<h3 style="color: #76b88a;">Strategy</h3>
						<p>To implement our vision of thrustworthy and credible parliamentary democracies through more transparency, education and participation, we focus on three closely connected strategical areas: <strong>technology</strong>, <strong>policy & advocacy</strong>, <strong>network</strong>.</p>
						<p><strong><u>Technology:</u></strong></p><p>Almost every parliament publishes video recordings and text proceedings of sessions. Together with other documents like legislative proposals, motions or inquiries, they allow direct insight into the decision processes of parliamentary democracy.</p>
						<p>But despite comparable structures and similar workflows, parliamentary proceedings are published in various, incompatible formats and parliament tv contents are only accessible via proprietary platforms. </p>
						<p>Videos recordings of parliamentary debates hold great untapped potential for digital democracy. They are more tangible than text documents or lists of votes and provide an easily accessible “window“ to the underlying discussions, negotiations and decisions. </p>
						<p>We want to use this potential by </p>
						<ul>
							<li>creating a joint technological basis for parliaments (interoperable standards, interfaces and document formats) as well as </li>
							<li>developing a search engine and interactive video platform, in which speeches are searchable, linkable, citable and sharable via a synchronisation of videos and proceedings. </li>
						</ul>
						<p>To enable more participation and dialogue, we create technical interfaces to existing civic democracy projects and allow journalists to connect parliamentary debates with background information, original sources or fact checks. </p>
						<p>All components will be published under open licenses and are optimised for decentral implementation. New parliaments can either be integrated into existing Open Parliament TV platforms or established independently. Via open data interfaces, all contents are still accessible, searchable and linkable beyond the boundaries of single parliaments. </p>
						<p><strong><u>Policy & Advocacy:</u></strong></p>
						<p>In many countries the digitisation of parliamentary workflows is hampered by a lack of resources, missing legal frameworks, outsourced know-how and outdated IT- and consulting structures. Additionally, political bodies and public administrations often have a limited understanding for the necessity as well as the structures for interparliamentary coordination and collaboration. </p>
						<p>The development of legal frameworks, the application of technological standards or the implementation of open data strategies is however not decided on administrative levels. </p>
						<p>This is why we advocate on all political levels for </p>
						<ul>
							<li>a legal right to Linked Open Data, </li>
							<li>open licencing of all parliamentary contents and data, </li>
							<li>access to and open licensing of all publicly funded software components (free and open source), </li>
							<li>compliance with and improvement of existing standards for technical interfaces, videos, documents and respective metadata, </li>
							<li>internal competence building for the systematic application of Linked Open Data principles, </li>
							<li>the publication and long-term archival of video recordings of parliamentary debates (where that is not yet the case), hearings and committee meetings as well as </li>
							<li>permanent public funding and operation of the required infrastructure and processes. </li>
						</ul>
						<p>We work hard to convince decision-makers, parliamentary groups and the bureaus of parliament to place these goals sustainably on the political agenda. </p>
						<p>We actively engage in Open Government work and join existing international coalitions, associations, networks and communities. We stand up for our vision and mission in working groups, committees and at public events, and raise awareness for the direct connection of transparent political processes and trustworthy, credible parliamentary democracies. </p>
						<p><strong><u>Network:</u></strong></p>
						<p>To effectively interlink political debates beyond parliamentary boundaries, we build up our own network, in which we focus specifically on the use of parliamentary data and video recordings from parliamentary debates. </p>
						<p>We create communication channels and regular discussion forums between </p>
						<ul>
							<li>linguists, social-, information- and political scientists, </li>
							<li>parliamentary administrations, </li>
							<li>journalists and news labs, </li>
							<li>Parliamentary Monitoring Organisations (PMOs) and </li>
							<li>open data activists </li>
						</ul>
						<p>who already work on document formats and standardised parliamentary data interfaces for many years in their respective organisations and communities.  </p>
						<p>As a network we advocate for creating interparliamentary collaboration structures and make sure that the different juristictions, languages, parliamentary systems and technological conditions are considered when implementing international standards. </p>
						<p>We actively support transnational and interdisciplinary knowledge transfer, facilitate a joint knowledge base and build up respective ressources.</p>
