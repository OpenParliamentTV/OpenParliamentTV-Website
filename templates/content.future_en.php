							<h3>5. Non-Profit Business Model</h3>
	            <p>The development and implementation of a sustainable <b>business model</b> is a central component of the 4-year scaling phase and will be developed in close cooperation between the project management and the new role to be filled in the area of business development. The ideas described below are therefore a preliminary draft, as is our <b>Business Model Canvas</b></p>
							<hr>
							<p><b>Principles</b></p>
							<p>In line with our goal of making parliamentary debates more transparent and accessible, all components of the Open Parliament TV platform will be published permanently for everyone under <b>free licences</b> and can be implemented and further developed in a decentralised manner. Our business model follows the principle of <b>political independence</b>, is <b>non-commercial</b>, and is subject to our <b><a href="https://openparliament.tv/code-of-conduct" target="_blank">Code of Conduct</a></b>.</p>
							<hr>
							<p><b>Financing</b></p>
							<p>In principle, the Open Parliament TV idea can be applied worldwide to all <b>national and regional parliaments</b> that publish video recordings and written proceedings of meetings (our technical architecture was designed for this from the start). Each parliament also increases the number of potential target groups and stakeholders. In addition, the <b>digitisation</b> of governmental processes, legislative <b>transparency, open data, strengthening democracy, and combating misinformation</b> are some of the most important topics of our time.</p>
	            <p>We see great <b>scaling potential</b> here, both for the expansion of our search engine and interactive video platform, and for the future funding of our work on Open Parliament TV.</p>
							<p>Over the past 5 years we have been supported by foundations, democracy funding programs, innovation funding and individual large donations. In the future we plan to finance <b>further development</b> through pilot projects, research projects, and public funding. From 2027, <b>ongoing operations</b> are to be financed in a non-profit business model by institutional users & donors, and until then, by foundations.</p>
	            <p>Within this framework, we will develop special <b>products</b> and <b>services</b> for specific target groups that follow the classic models for monetising open source projects and are intended to make us financially independent of funding in the long term.  </p>
							<p><b>Products</b></p>
							<ul>
								<li>Plattform services</li>
								<ul>
									<li>Extended export formats (e.g. interface to video production tools)</li>
									<li>Video annotations tool</li>
									<li>Uploading custom materials / additional documents in protected areas and private groups (e.g. for school classes and political seminars)</li>
								</ul>
							</ul>
							<ul>
								<li>White-label solutions</li>
								<ul>
									<li>Parliamentary media library as a service</li>
									<li>Video player and social media snippets with custom branding</li>
								</ul>
							</ul>
							<ul>
								<li>Politics monitoring solutions</li>
								<ul>
									<li>Automated summaries of debates</li>
									<li>Notifications for keywords in new debates</li>
									<li>Notifications when terms accumulate</li>
									<li>Individual statistics and evaluations</li>
								</ul>
							</ul>
							<p>We are also considering the launch of a secondary platform, separate from the search engine and interactive video platform for parliamentary debates, which will in particular make the annotation functionalities (linking videos with additional information at specific times) and the synchronisation of videos and transcripts accessible for other domains (e.g. teaching / learning videos, explanatory videos, lecture recordings, news broadcasts and political magazines). </p>
							<p><b>Services</b></p>
							<ul>
								<li>Custom developments</li>
								<li>Custom interfaces / integrations (e.g. in editorial systems)</li>
								<li>Plug-ins for existing tools / software</li>
								<li>Advice & consulting for parliamentary administrations
	(open data standards, interoperability, interfaces, document formats)</li>
							</ul>
	            			<p>Furthermore, in cooperation with <b>media partners</b>, we plan joint research and content analysis, fact checks on parliamentary debates, experimental storytelling formats and data visualisations, as well as the development of campaign tools and widgets, before upcoming elections and during important debates.</p>
