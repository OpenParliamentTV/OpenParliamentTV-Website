							<h3>4. Target Groups and Stakeholders</h3>
	            <p>Open Parliament TV is primarily a tool for <b>journalists</b> and <b>activists</b>. Indirectly, we want to reach <b>politically interested citizens</b> via these two target groups (see also <a href="#impact-logic">3. Impact</a>).</p>
	            <p><b>Journalists</b> and <b>activists</b> (specifically: watchdog organisations) classify processes in parliaments, participate in critical analyses and discussions of political positions and put the statements of members of parliament into new contexts.</p>
							<p>However, the way videos of speeches in parliament are currently published is largely based upon sharing short key moments with a clear lack of contextual information like a link to the full debate, relevant original documents, or additional materials. This means that video clips are used to share short moments, but as soon as the speeches are used for in-depth analyses, background articles, fact checks, or longform reporting, the textual proceedings are the basis. The main reason for this is that the videos can neither be searched by keywords nor can specific statements be cited or directly linked. </p>
							<p>We are making videos of parliamentary debates more usable for journalists by making it easier to <b>find, share, quote</b> and <b>embed</b> video excerpts from parliamentary speeches, enabling the embedding of <b>current reporting</b> in speeches, and offering tools for the statistical <b>evaluation</b> and <b>analysis</b> of parliamentary debates</p>
							<p>In the future, political discourse on our search engine and interactive video platform will be linked and digitally accessible across parliamentary, national and linguistic borders in cooperation with <b>parliamentary administrations</b>.</p>
							<p>In addition to the primary target groups and stakeholders, <b>political groups, research groups</b>, and <b>educational institutions</b> are important stakeholders due to their ongoing work with parliamentary content.</p>
	            <p>For example, <b>research groups</b> have been working in international consortia for many years on the issue of interoperability and standardisation of parliamentary content and processes (keyword: machine-readable plenary minutes and legislative procedures).</p>
	            <p>Through pilot projects and cooperations with <b>educational institutions</b>, we will develop tools and didactic concepts for the manual annotation / linking of speeches with teaching / learning materials. This will improve the usability of parliamentary content in the field of political education.</p>
