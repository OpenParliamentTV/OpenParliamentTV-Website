							<h3>3. Impact</h3>
							<p>In order to better integrate democratic processes and means of participation into our digitised everyday lives, they must be easily, promptly and directly accessible to everyone.</p>
							<p>In this context, videos of parliamentary debates serve as a tangible, contemporary, and up-to-date interface to the work of parliaments. The recordings and live broadcasts are not just materials for journalists or parliamentary staff, but a direct implementation of the credo "the parliament negotiates in public".</p>
	            <p>With Open Parliament TV we are strengthening <b>trust in parliamentary democracy</b> by</p>
							<ul>
	              <li>making parliamentary debates <b>more accessible</b> and <b>easier to use</b></li>
	              <li>increasing the overall <b>visibility</b> of parliamentary decision processes </li>
							</ul>
							<p>Ultimately, our aim is to reach politically interested citizens and the general public. However, since we do not classify content ourselves and the contextualization with additional content is limited to automated procedures, we focus our logic model on the two target groups of journalists and activists (see also <a href="#target-groups">4. Target Groups and Stakeholders</a>). Through analyses, research, background reports, fact-checking formats, and campaigns, they embed the debates in a content context that we, as Open Parliament TV, cannot and do not want to offer.</p>
							<p>We see our strength in the development and provision of technological solutions as a "tool" for working with parliamentary content. Our search engine and interactive video platform for parliamentary debates is a building block of a newly created <b>"basic architecture" of digital democracy</b>, which offers independent and inter-parliamentary access to content.</p>
							<p><b>Indirect Impact</b></p>
							<p>Our work has an indirect impact. By journalists and activists using our tools, the parliamentary process as a whole gains more visibility. We are convinced that we can ultimately reach significantly more people in this way and have a much broader and more sustainable impact in line with our <b>logic</b> model. Focusing our work on more clearly defined target groups also makes it easier for us to set clear and measurable (SMART) goals.</p>
