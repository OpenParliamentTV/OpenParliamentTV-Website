		<section id="what-is-open-parliament-tv">
			<div class="container">
				<div class="row">
					<div class="col-lg-9 mx-auto card px-1">
						<div class="card-body">
							<h3>1. What is Open Parliament TV?</h3>
							<p>Open Parliament TV is first and foremost a <b>search engine and interactive video platform for parliamentary debates</b>. The core of our platform is a technical architecture that automatically <b>synchronises the video recordings of the speeches with the official text proceedings</b>.</p>
	            <p>This allows us to make the videos searchable word by word in a <b>full text search</b>. Via the synchronisation of video recordings and written proceedings, we can additionally provide:</p>
							<ul>
	              <li><b>interactive transcripts</b> (click on a sentence > jump to point of time in the video)</li>
	              <li><b>context-related annotations</b> (additional information on mentioned people, organisations, topics, laws and documents directly in the video player)</li>
	              <li>improved means of <b>participation</b> (quoting, embedding and sharing specific video segments in the context of the full speech)</li>
							</ul>
	            <p>Open Parliament TV significantly simplifies <b>finding, sharing</b> and <b>citing</b> video snippets from parliamentary speeches. Based on single terms or sentence fragments, the relevant video snippets can be found in split seconds, played and then embedded as a quote in other platforms.</p>
	            <p>Besides the full text search, Open Parliament TV includes <b>additional entry points</b> into the debates, like finding speeches via the profile page of a faction or watching all speeches in which a specific document or law is mentioned.</p>
							<p>Open Parliament TV is <b><a href="https://github.com/OpenParliamentTV" target="_blank">open source</a></b> and <b>inter-parliamentary</b>. The entire architecture was designed from the start to be transferable to all parliaments that publish video recordings as well as written proceedings of the sessions.</p>
							<p>In October 2021, the German Bundestag became the first parliament to be accessible on Open Parliament TV. More than 60,000 speeches from over 10 years of Bundestag history are now accessible at <a href="https://de.openparliament.tv" target="_blank" >https://de.openparliament.tv</a> and the number is growing every day.</p>
